dia_da_semana = int(input("Informe o dia da semana em numeros de 1 a 7 :"))

if dia_da_semana == 1:
    print("SEGUNDA - FEIRA")
else:
    if dia_da_semana == 2:
        print("TERCA - FEIRA")
    else:
        if dia_da_semana == 3:
            print("QUARTA - FEIRA")
        else:
            if dia_da_semana == 4:
                print("QUINTA - FEIRA")
            else:
                if dia_da_semana == 5:
                    print("SEXTA - FEIRA")
                else:
                    if dia_da_semana == 6:
                        print("SABADO")
                    else:
                        if dia_da_semana == 7:
                            print("DOMINGO")
                        else:
                            print("NUMERO INVALIDO, POR FAVOR DECLARE UM VALOR DE 1 A 7")